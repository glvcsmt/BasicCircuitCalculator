    private void CalcActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_CalcActionPerformed
       
        Ug.removeAll();
        Ig.removeAll();
        R1.removeAll();
        R2.removeAll();
        R3.removeAll();
        
        UR1.removeAll();
        UR2.removeAll();
        UR3.removeAll();
        UIg.removeAll();
        I2.removeAll();
        I3.removeAll();
        
        float ug = 0, ig = 0, r1 = 0, r2 = 0, r3 = 0;
        
        try {
            
            ug = Float.parseFloat(Ug.getText()); ig = Float.parseFloat(Ig.getText()); 
            r1 = Float.parseFloat(R1.getText()); r2 = Float.parseFloat(R2.getText());
            r3 = Float.parseFloat(R3.getText());
            
            float ur1 = ig * r1, 
                  i2 = (ug + r3 * ig)/(r2 + r3),
                  i3 = ig - i2,
                  ur2 = i2 * r2,
                  ur3 = i3 * r3,
                  uig = (-ur1 - ur2);
            
        UR1.setText(ur1 + " [V]");
        UR2.setText(ur2 + " [V]");
        UR3.setText(ur3 + " [V]");
        UIg.setText(uig + " [V]");
        I2.setText(i2 + " [A]");
        I3.setText(i3 + " [A]");
        
        Ug.setText("");
        Ig.setText("");
        R1.setText("");
        R2.setText("");
        R3.setText("");
            
        } catch (Exception e) {
            JOptionPane.showInternalMessageDialog(rootPane, "Mindenhol sz√°moknak kell szerepelnie!", "Hiba!", JOptionPane.ERROR_MESSAGE);
        }        
    }//GEN-LAST:event_CalcActionPerformed

